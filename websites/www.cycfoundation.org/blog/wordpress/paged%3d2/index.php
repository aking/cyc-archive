<h1 id="post-25"><a href="http://www.cycfoundation.org/blog/?p=25" rel="bookmark" title="Permanent Link: First Skypecast">First Skypecast</a></h1>

<div class="entry">

<p><em>Originally posted on 5/26/2006 by John De Oliveira</em></p>
<p>We had 10 programmers from around the country on a Skypecast (conference call) for 90 minutes last night discussing creating web services for Cyc. We&#8217;ve got some issues to work out with microphones and with getting on the call in the first place. Still, it was a worthwhile call.</p>
<p>We&#8217;ll be talking again next Wednesday (May 31, 2006) at 8:30pm Central Standard Time. Central time is one hour earlier than the East Coast and two hours later than the West Coast. It&#8217;s a public call, so anyone can join, but we usually mute people to improve sound quality. There&#8217;s chat that happens alongside the audio, and we may use VNC to do screen sharing next time. To join the call, you first have to join Skype (for free). Go to www.skype.com. Then, on the Skype home page, there&#8217;s a link for skypecasts. Then look for &#8220;Find a Skypecast&#8221; &#8220;starting soon&#8221;. Or you can just visit the link: https://skypecasts.skype.com/skypecasts/upcoming</p>
<p>We don&#8217;t need to get your skype address ahead of time. You just click your way into the call. Once in, we may add you to a group chat, and if you want to participate in Cyclify, you can put your contact info into chat.</p>
<p>Hope to see you there!</p>
<p>P.S. Please test how your microphone sounds before joining the call. Do this by placing a test call to the skype number: echo123. Most people have their mikes way too close at first, and once you&#8217;re on the call it&#8217;s hard to tell.</p>
</div>

