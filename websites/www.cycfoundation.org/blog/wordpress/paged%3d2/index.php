<h1 id="post-22"><a href="http://www.cycfoundation.org/blog/?p=22" rel="bookmark" title="Permanent Link: But that&#8217;s not how people think&#8230;">But that&#8217;s not how people think&#8230;</a></h1>

<div class="entry">

<p><em>Originally posted on 4/21/2006 by John De Oliveira</em></p>
<p>I&#8217;ve often heard that complaint when I describe that Cyc uses deduction to do reasoning. That&#8217;s not how people think. First of all, I don&#8217;t want to claim that Cyc &#8220;thinks&#8221; at all, but sometimes its expedient to use expressions like that.</p>
<p>As for how people think, who knows? There&#8217;s a lot of research in that area, but we really don&#8217;t know yet how people think. I suspect it&#8217;s quite different than the processing that goes on in the Cyc inference engine. But who cares? It was never a goal to emulate human beings; it was merely a goal to in some sense understand the world of humans.</p>
<p>Consider the airplane. It is the man-made version of a bird, only faster, more powerful and controllable. Where are its feathers? Why don&#8217;t its wings flap?</p>
<p>Many of man&#8217;s tools serve the purpose of amplifying some existing capability. The telephone amplifies the ear and mouth. The bicycle amplifies power of the legs. A technology such as Cyc can amplify the power of the mind even if it doesn&#8217;t work at all like one.</p>
</div>

