<h1 id="post-27"><a href="http://www.cycfoundation.org/blog/?p=27" rel="bookmark" title="Permanent Link: The Usability Barrier">The Usability Barrier</a></h1>

<div class="entry">

<p><em>Originally posted on 8/1/2006 by John De Oliveira</em></p>
<p>At the AAAI conference in Boston, Peter Norvig of Google <a href="http://www.zdnet.com.au/news/software/soa/Google_exec_challenges_Berners_Lee/0,2000061733,39263931,00.htm">raised a concern</a> about the Semantic Web to Tim Berners-Lee. The concern has primarily to do with ease of use.</p>
<p>We&#8217;re not going to have a smarter Web until and unless the new tools are easy enough to use. Until then, we&#8217;ll have Web 2.0, as described <a href="http://www.oreillynet.com/xml/blog/2006/06/why_the_world_is_ready_for_the.html">here</a>. As Zombini explains, these Web 2.0 technologies already have semantic qualities, but they are held back by &#8220;using older, more limited technologies because of our lack of understanding and desire to learn complex new languages.&#8221;</p>
