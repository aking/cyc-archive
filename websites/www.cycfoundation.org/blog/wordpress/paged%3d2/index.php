<h1 id="post-26"><a href="http://www.cycfoundation.org/blog/?p=26" rel="bookmark" title="Permanent Link: Cyc Foundation Update">Cyc Foundation Update</a></h1>

<div class="entry">

<p><em>Originally posted on 8/1/2006 by John De Oliveira</em></p>
<p>A lot has been happening at the Cyc Foundation, even though nothing has been happening with this blog. Some folks have expressed confusion over where to go for Cyc Foundation information. People who want to get their hands dirty and participate can just email me (johnd at cycfoundation.org). Tell me a little about yourself and why your interested, and we&#8217;ll add you to the mailing list and get you access to the wiki.</p>
<p>Better yet, we now have a server that is operational. So, the logical next step will be to put up a website. We&#8217;re working on it, along with several other projects.</p>
<ul>
<li>We&#8217;re making an application that will let people (and some background programs) determine links between Wikipedia article titles and Cyc concept terms. This will grow the Cyc ontology and provide a way to access Wikipedia content semantically.</li>
<li>We&#8217;re getting ready to publish the Cyc ontology as a Semantic Web resource, with one URI per concept.</li>
<li>We&#8217;re creating REST Web services for easy access to create, read, update, delete and query Cyc concepts, facts and rules.</li>
</ul>
<p>Further descriptions of all of these are available on the members-only Wiki and will also be available on the website.</p>
</div>

