<div class="entry">

<p><em>Originally posted on 5/20/2006 by John De Oliveira</em></p>
<p>If you ever wonder why it is so difficult for computers to understand natural language, check out this <a href="http://www.kurzweilai.net/meme/frame.html?main=/articles/art0186.html">article by George Miller</a>, creator of WordNet. In one example from his article, he points out that the following couplet from a Robert Frost poem . . .</p>
<p>&#8220;But I have promises to keep, and miles to go before I sleep.&#8221;</p>
<p>. . . has over 3 trillion possible meanings.</p>
