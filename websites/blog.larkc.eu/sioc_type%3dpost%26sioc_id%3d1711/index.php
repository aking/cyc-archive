	<sioc:content>(by Zhisheng Huang)

My China journey started on August 15 and ended on Sept 15, 2009. Because of the H1N1 flu control in China,  I was requested to stay at home for one week and not allowed to enter any university campus after my arrival in China. Thus, I took the flight to  the city where my parents are living  after I arrived in Beijing on August 15.

My dissemination activities started on August 23, 2009 actually. From August 23 till Sept 15, I gave eighteen lectures and invited talks in China within the three weeks. Through the journey, I was surprised by the phenomenon that Chinese researchers and students are so eager  to learn logics and  the semantic technology.  I was really touched by the younger generation of the Chinese,  who are  keen to  learn the knowledge of modern science and technology.

My first activity was to give a series of lectures in the Chinese Semantic Web Summer School in Nanjing from August 24 till August 28.  More than 110 researchers and students who are from about 50 China universities attended the summer school. Most of them are PhD students. Many students had to pay the accommodation and the travel expenses by themselves, without any financial support from their own universities. My  eight lectures in the summer school cover the topics of  i) Introduction to modern logics,  ii) Introduction to Description Logics, iii) Decidability and complexity of Description Logics, iv) Knowledge representation and reasoning, v) Introduction to the Semantic Web, vi) Ontology reasoning and management, vii) LarKC and Web scale reasoning, and  viii) Research topics in the Semantic Web.

From August 29 till August 31 I co-chaired the Chinese Semantic Web Symposium (CSWS09), which was co-located with the Chinese Semantic Web Summer School 2009. CSWS09 was held   in the beautiful campus of the Southeast University, Nanjing. About 100 Chinese researchers/scientists attended the conference.  18 papers out of 95 submissions were accepted for the paper presentation in CSWS09. That made CSWS  one of the highly competitive  conferences in the Semantic Web. The scalability and Web scale reasoning were frequently discussed in the talks and the panel of CSWS09.

After my visit in Nanjing, I went to visit Jiangsu University of Science and Technology in Zhenjiang, a city where it is not very far away from Nanjing. In Zhenjiang I gave three lectures  and an invited talk  to talk about LarKC and its relevant technologies (i.e. the Semantic Web and ontology engineering) from Sept 1 till Sept 8.

My visit in Beijing (from Sept 9 till Sept 15) was a very busy week in my China journey.
That includes an invited talk in the Institute of Scientific and Technical Information of China in the afternoon of Sept 10, an invited talk in Beijing young logician seminar in the afternoon of Sept 12, an invited talk in Tsinghua  University, one of the top universities in China in the afternoon of Sept 14, and  a series of lectures in the International Web Intelligence Institute (WICI), Beijing University of Technology, one of the partners in the LarKC Consortium.

